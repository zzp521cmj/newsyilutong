package com.jy.model;
/*
 * 理赔结算驳回记录表
 */
public class Settlement_Record {
	private String record_id;//驳回记录id
	private String claimsid;//理赔id
	private String abnormalid;
	 private String numbersid;//理赔单号
	 private String compensation_payments;//理赔金
	 private String claims_state;//理赔状态
	 private String claims_time; //理赔发起时间
	 private String damage_intensity;//事故分析及处理结果
	 private String verifier;//审核人
	 private String audit_opinion;//审核意见
	 private String submit_state;//提交状态
	 private String reason;//驳回理由
	 private String reject_date;//驳回时间
	 
	 
	public String getAbnormalid() {
		return abnormalid;
	}
	public void setAbnormalid(String abnormalid) {
		this.abnormalid = abnormalid;
	}
	public String getRecord_id() {
		return record_id;
	}
	public void setRecord_id(String record_id) {
		this.record_id = record_id;
	}
	public String getClaimsid() {
		return claimsid;
	}
	public void setClaimsid(String claimsid) {
		this.claimsid = claimsid;
	}
	public String getNumbersid() {
		return numbersid;
	}
	public void setNumbersid(String numbersid) {
		this.numbersid = numbersid;
	}
	public String getCompensation_payments() {
		return compensation_payments;
	}
	public void setCompensation_payments(String compensation_payments) {
		this.compensation_payments = compensation_payments;
	}
	public String getClaims_state() {
		return claims_state;
	}
	public void setClaims_state(String claims_state) {
		this.claims_state = claims_state;
	}
	public String getClaims_time() {
		return claims_time;
	}
	public void setClaims_time(String claims_time) {
		this.claims_time = claims_time;
	}
	public String getDamage_intensity() {
		return damage_intensity;
	}
	public void setDamage_intensity(String damage_intensity) {
		this.damage_intensity = damage_intensity;
	}
	public String getVerifier() {
		return verifier;
	}
	public void setVerifier(String verifier) {
		this.verifier = verifier;
	}
	public String getAudit_opinion() {
		return audit_opinion;
	}
	public void setAudit_opinion(String audit_opinion) {
		this.audit_opinion = audit_opinion;
	}
	public String getSubmit_state() {
		return submit_state;
	}
	public void setSubmit_state(String submit_state) {
		this.submit_state = submit_state;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getReject_date() {
		return reject_date;
	}
	public void setReject_date(String reject_date) {
		this.reject_date = reject_date;
	}
	
	 
}
