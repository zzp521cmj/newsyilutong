package com.jy.model;

public class Warehouse {
	private String warehouse_id;
	private String warehouse_num;//仓库编号
	private String warehouse_name;//仓库姓名
	private	String warehouse_address;//仓库地址
	private String warehouse_type;//仓库类型
	private String warehouse_point;//仓库坐标
	private String warehouse_createtime;//创建时间
	public String getWarehouse_id() {
		return warehouse_id;
	}
	public void setWarehouse_id(String warehouse_id) {
		this.warehouse_id = warehouse_id;
	}
	public String getWarehouse_num() {
		return warehouse_num;
	}
	public void setWarehouse_num(String warehouse_num) {
		this.warehouse_num = warehouse_num;
	}
	public String getWarehouse_name() {
		return warehouse_name;
	}
	public void setWarehouse_name(String warehouse_name) {
		this.warehouse_name = warehouse_name;
	}
	public String getWarehouse_address() {
		return warehouse_address;
	}
	public void setWarehouse_address(String warehouse_address) {
		this.warehouse_address = warehouse_address;
	}
	public String getWarehouse_type() {
		return warehouse_type;
	}
	public void setWarehouse_type(String warehouse_type) {
		this.warehouse_type = warehouse_type;
	}
	public String getWarehouse_point() {
		return warehouse_point;
	}
	public void setWarehouse_point(String warehouse_point) {
		this.warehouse_point = warehouse_point;
	}
	public String getWarehouse_createtime() {
		return warehouse_createtime;
	}
	public void setWarehouse_createtime(String warehouse_createtime) {
		this.warehouse_createtime = warehouse_createtime;
	}
	
	
}
